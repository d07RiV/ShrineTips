  uint32 transform(uint8_const_ptr* ptr, uint32* table)
  {
    if ((**ptr & 0x80) && (**ptr & 0xF8) != 0xF8) {
      uint32 result = 0;
      uint8 head = **ptr;
      uint8* dst = (uint8*)&result;
      while ((head & 0x80) && **ptr) {
        if (table)
          table = (uint32*)table[**ptr];
        *dst++ = *(*ptr)++;
        head <<= 1;
      }
      if (table && (head & 0x80) == 0) {
        result = (uint32)table;
      }
      return result;
    } else {
      if (table && table[**ptr]) {
        return table[*(*ptr)++];
      }
      return *(*ptr)++;
    }
  }

  uint8_const_ptr next(uint8_const_ptr ptr)
  {
    if ((*ptr & 0x80) && (*ptr & 0xF8) != 0xF8) {
      uint8 head = *ptr;
      while ((head & 0x80) & *ptr) {
        head <<= 1;
        ptr++;
      }
      return ptr;
    } else {
      return ptr + 1;
    }
  }

  uint32 parse(uint32 cp) {
    if ((cp & ~0x7F) == 0) {
      return cp;
    }
    uint32 result = (cp & 0xFF);
    uint32 mask = 0x40;
    cp >>= 8;
    while (cp) {
      result = (result << 6) | (cp & 0x3F);
      cp >>= 8;
      mask <<= 5;
    }
    return result & (mask - 1);
  }

